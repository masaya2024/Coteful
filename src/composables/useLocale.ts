function interpolate(template: string, params?: Record<string, string | number>) {
  if (!params) {
    return template
  }

  return template.replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? ''))
}

export function useLocale() {
  const locale = useCookie<Locale>('conteful-locale', {
    default: () => 'ja',
    sameSite: 'lax',
  })

  const locales = [
    { value: 'ja' as const, labelKey: 'lang.ja' },
    { value: 'en' as const, labelKey: 'lang.en' },
  ]

  function setLocale(next: Locale) {
    locale.value = next
  }

  function t(key: string, params?: Record<string, string | number>) {
    const pack = messages[locale.value] ?? messages.ja
    const fallback = messages.en[key] ?? key
    const value = pack[key] ?? fallback

    if (typeof value === 'function') {
      return value(params)
    }

    return interpolate(value, params)
  }

  return {
    locale,
    locales,
    setLocale,
    t,
  }
}
